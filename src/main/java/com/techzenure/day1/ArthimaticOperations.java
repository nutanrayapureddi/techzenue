package com.techzenure.day1;

public class ArthimaticOperations {

	public static void main(String[] args) {
		int x = 10;
		int y = 5;
		System.out.println(x+y);//15
		System.out.println(x-y);//5
		System.out.println(x/y);//2
		System.out.println(x%y);//0 only remainder will come and if first operator has negative then answer is negative
		System.out.println(x*y);//50
		System.out.println(10.0f/3);
		System.out.println(10.0d/3);
		//System.out.println(10/0);
		System.out.println(10.0f/0);
		System.out.println(-10.0f/0);
		System.out.println(0.0/0);//nan: not a number
		

	}

	}

